#include <stdio.h>
int	ft_toupper(int c)
{
	if (c >= 'a' && c <= 'z')
	{
		c = c - 32;
	}
	return (c);
}

int main()
{
	int x;

	x = 'i';
	printf("%d", ft_toupper(x));
	return 0;
}

