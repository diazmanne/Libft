#include "libft.h"

char		*ft_strdup(const char *s)
{
	int		l;
	int		i;
	char	*p;

	l = 0;
	while (s[l])
	{
		l++;
	}
	p = (char *)malloc(sizeof(*p * (l + 1)));
	i = 0;
	while (i < l)
	{
		p[i] = s[i];
		i++;
	}
	free(p);
	return (p);
}
