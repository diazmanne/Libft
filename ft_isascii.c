#include <stdio.h>
int	ft_isascii(int c)
{
	return (c >= 0 && c <= 127 ? 1 : 0); 
}

int main ()
{
	int n;
	
	n = 'E';
	printf("%d", ft_isascii(n));
	return 0;
}
